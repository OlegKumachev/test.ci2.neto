import byField from './game.js';

test('test', () => {
  const usersCopy = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const expected = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];

  const result = byField('health', usersCopy)
  expect(result).toEqual(expected);
})
